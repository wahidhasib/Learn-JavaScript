/**
 * ============================================================
 * 📘 Switch Statement
 * ============================================================
 *
 * multiple condition handle করার জন্য use হয়
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const day = "monday";

switch (day) {
  case "monday":
    console.log("Start of week");
    break;

  case "friday":
    console.log("Weekend is near");
    break;

  case "sunday":
    console.log("Holiday");
    break;

  default:
    console.log("Normal day");
}

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * break না দিলে next case execute হবে
 */

