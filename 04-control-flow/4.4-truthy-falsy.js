/**
 * ============================================================
 * 📘 Truthy & Falsy Values
 * ============================================================
 *
 * JavaScript এ কিছু value automatically true/false হয়
 */

/**
 * ============================================================
 * ❌ FALSY VALUES
 * ============================================================
 *
 * false
 * 0
 * ""
 * null
 * undefined
 * NaN
 */

if (0) {
  console.log("This will NOT run");
}

/**
 * ============================================================
 * ✅ TRUTHY VALUES
 * ============================================================
 *
 * সব কিছু যা falsy না
 */

if ("Hello") {
  console.log("This will run");
}

