/**
 * ============================================================
 * 📘 Control Flow: if - else
 * ============================================================
 *
 * Control flow মানে:
 * → কোন condition এর উপর ভিত্তি করে code চালানো
 */

/**
 * ============================================================
 * 🧩 Basic if
 * ============================================================
 */

const age = 16;

if (age >= 18) {
  console.log("You are an adult");
}

/**
 * ============================================================
 * 🧩 if - else
 * ============================================================
 */

if (age >= 18) {
  console.log("Adult");
} else {
  console.log("Minor");
}

/**
 * ============================================================
 * 🧩 if - else if - else
 * ============================================================
 */

const score = 60;

if (score >= 80) {
  console.log("A+");
} else if (score >= 70) {
  console.log("A");
} else if (score >= 60) {
  console.log("B");
} else {
  console.log("Fail");
}

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * if-else → condition based decision making
 *
 * ============================================================
 */
