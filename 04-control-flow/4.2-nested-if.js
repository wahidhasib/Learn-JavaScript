/**
 * ============================================================
 * 📘 Nested if
 * ============================================================
 *
 * if এর ভিতরে if ব্যবহার করা
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const age = 25;
const hasId = false;

if (age >= 18) {
  if (hasId) {
    console.log("You can enter");
  } else {
    console.log("ID required");
  }
} else {
  console.log("Too young");
}

