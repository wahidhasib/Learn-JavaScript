/**
 * ============================================================
 * 📘 Return Keyword
 * ============================================================
 *
 * Function থেকে value ফেরত পাঠাতে use হয়
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

function add(a, b) {
  return a + b;
}

const result = add(10, 20);

console.log(result);

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * return এর পরে code execute হয় না
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

function test() {
  return "Hello";

  // নিচের code run হবে না
  console.log("World");
}

console.log(test());

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * return:
 * → function output দেয়
 *
 * ============================================================
 */
