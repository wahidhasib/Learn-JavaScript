/**
 * ============================================================
 * 📘 Function Expression
 * ============================================================
 *
 * Variable এর মধ্যে function store করা
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const sum = function () {
  console.log(10 + 20);
};

sum();

/**
 * ============================================================
 * 🧩 Function with Parameters
 * ============================================================
 */

const multiply = function (a, b) {
  console.log(a * b);
};

multiply(5, 6);

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * Function expression hoisted হয় না
 */

