/**
 * ============================================================
 * 📘 Rest Operator (...)
 * ============================================================
 *
 * Multiple arguments array হিসেবে নেয়
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

function total(...numbers) {
  console.log(numbers);

  let sum = 0;

  for (const number of numbers) {
    sum += number;
  }

  return sum;
}

console.log(total(10, 20, 30, 10));

/**
 * ============================================================
 * 🧩 Example 2
 * ============================================================
 */

function total2(numbers) {
  console.log(numbers);

  let sum = 0;

  for (const number of numbers) {
    sum += number;
  }

  return sum;
}

console.log(total2([10, 20, 30]));

