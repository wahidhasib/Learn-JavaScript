/**
 * ============================================================
 * 📘 Default Parameters
 * ============================================================
 *
 * Parameter এর default value set করা
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

function greet(name = "Guest") {
  console.log("Hello", name);
}

greet();

greet("Hasib");

