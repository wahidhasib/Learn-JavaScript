/**
 * ============================================================
 * 📘 Callback Function
 * ============================================================
 *
 * Function কে argument হিসেবে পাঠানো
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

function processUser(name, callback) {
  console.log("Processing your order:", name);

  return callback();
}

function complete() {
  console.log("Order completed!");
}

processUser("Hasib", complete);

