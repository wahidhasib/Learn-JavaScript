const list = document.getElementById("list");

/**
 * ============================================================
 * 🧩 Event Delegation
 * ============================================================
 */

list.addEventListener("click", function (event) {
  if (event.target.tagName === "LI") {
    console.log(event.target.textContent);
  }
});

/**
 * ============================================================
 * 🎯 BENEFIT
 * ============================================================
 *
 * → better performance
 * → dynamic element support
 */

