/**
 * ============================================================
 * 📘 Selecting DOM Elements
 * ============================================================
 */

/**
 * ============================================================
 * 🧩 getElementById()
 * ============================================================
 */

const title = document.getElementById("title");

console.log(title);

/**
 * ============================================================
 * 🧩 querySelector()
 * ============================================================
 */

const description = document.querySelector(".description");

console.log(description);

/**
 * ============================================================
 * 🧩 querySelectorAll()
 * ============================================================
 */

const allLi = document.querySelectorAll("li");

console.log(allLi);

