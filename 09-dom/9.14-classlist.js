/**
 * ============================================================
 * 📘 classList
 * ============================================================
 */

const title = document.getElementById("title");

/**
 * ============================================================
 * 🧩 add()
 * ============================================================
 */

title.classList.add("active");

/**
 * ============================================================
 * 🧩 remove()
 * ============================================================
 */

title.classList.remove("active");

/**
 * ============================================================
 * 🧩 toggle()
 * ============================================================
 */

title.classList.toggle("dark");

