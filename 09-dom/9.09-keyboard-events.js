/**
 * ============================================================
 * 📘 Keyboard Events
 * ============================================================
 */

const input = document.getElementById("username");

/**
 * ============================================================
 * 🧩 keydown
 * ============================================================
 */

input.addEventListener("keydown", function (event) {
  console.log(event.key);
});

/**
 * ============================================================
 * 🧩 keyup
 * ============================================================
 */

input.addEventListener("keyup", function () {
  console.log("Key Released");
});

