/**
 * ============================================================
 * 📘 Form Events
 * ============================================================
 */

const form = document.getElementById("form");

/**
 * ============================================================
 * 🧩 submit event
 * ============================================================
 */

form.addEventListener("submit", function (event) {
  event.preventDefault();

  console.log("Form Submitted");
});

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * preventDefault():
 * → page reload stop করে
 */

