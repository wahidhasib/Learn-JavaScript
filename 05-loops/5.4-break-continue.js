/**
 * ============================================================
 * 📘 break & continue
 * ============================================================
 */

/**
 * ============================================================
 * 🧩 break
 * ============================================================
 *
 * loop stop করে
 */

for (let i = 1; i <= 10; i++) {
  if (i === 5) {
    break;
  }

  console.log(i);
}

/**
 * ============================================================
 * 🧩 continue
 * ============================================================
 *
 * current iteration skip করে
 */

for (let i = 1; i <= 5; i++) {
  if (i === 3) {
    console.log(`Your current number is:${i}`);
    continue;
  }

  console.log(i);
}

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * break:
 * → loop terminate করে
 *
 * continue:
 * → current iteration skip করে
 *
 * ============================================================
 */
