/**
 * ============================================================
 * 📘 JavaScript For Loop
 * ============================================================
 *
 * Loop ব্যবহার করা হয়:
 * → একই কাজ বারবার করার জন্য
 */

/**
 * ============================================================
 * 🧩 Basic for loop
 * ============================================================
 *
 * Syntax:
 *
 * for(initialization; condition; increment/decrement)
 */

for (let i = 1; i <= 10; i += 2) {
  console.log("Current Number:", i);
}

/**
 * ============================================================
 * 🧩 Loop Execution Step
 * ============================================================
 *
 * Step 1:
 * → let i = 1
 *
 * Step 2:
 * → condition check
 *
 * Step 3:
 * → code execute
 *
 * Step 4:
 * → i++
 *
 * আবার condition check হবে
 */

/**
 * ============================================================
 * 🧩 Reverse Loop
 * ============================================================
 */

for (let i = 5; i >= 1; i--) {
  console.log("Reverse:", i);
}

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * for loop:
 * → fixed iteration এর জন্য ভালো
 *
 * ============================================================
 */
