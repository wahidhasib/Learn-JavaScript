/**
 * ============================================================
 * 📘 While Loop
 * ============================================================
 *
 * যতক্ষণ condition true থাকবে
 * ততক্ষণ loop চলবে
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

let i = 1;

while (i <= 5) {
  console.log("Number:", i);

  i++;
}

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * increment/decrement ভুলে গেলে
 * infinite loop হবে
 */

