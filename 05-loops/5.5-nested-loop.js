/**
 * ============================================================
 * 📘 Nested Loop
 * ============================================================
 *
 * Loop এর ভিতরে loop
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

for (let row = 1; row <= 3; row++) {
  console.log("Row:", row);

  for (let col = 1; col <= 3; col++) {
    console.log("Column:", col);
  }
}

/**
 * ============================================================
 * 🧠 REAL-LIFE ANALOGY
 * ============================================================
 *
 * School:
 *
 * Row 1 → Student 1, 2, 3
 * Row 2 → Student 1, 2, 3
 */

