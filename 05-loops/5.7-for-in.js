/**
 * ============================================================
 * 📘 for...in Loop
 * ============================================================
 *
 * Object এর key loop করার জন্য use হয়
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const user = {
  name: "Hasib",
  age: 25,
  country: "Bangladesh",
};

for (const key in user) {
  console.log(key);
}

/**
 * ============================================================
 * 🧩 Access Object Value
 * ============================================================
 */

for (const key in user) {
  console.log(key, ":", user[key]);
}

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * for...in:
 * → object keys return করে
 *
 * for...of:
 * → values return করে
 */

