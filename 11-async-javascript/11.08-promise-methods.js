/**
 * ============================================================
 * 📘 Promise Methods
 * ============================================================
 */

const p1 = Promise.resolve("Promise 1");

const p2 = Promise.resolve("Promise 2");

const p3 = Promise.resolve("Promise 3");

/**
 * ============================================================
 * 🧩 Promise.all()
 * ============================================================
 */

Promise.all([p1, p2, p3])

  .then(function (data) {
    console.log(data);
  });

/**
 * ============================================================
 * 🧩 Promise.race()
 * ============================================================
 */

Promise.race([p1, p2, p3])

  .then(function (data) {
    console.log(data);
  });

