/**
 * ============================================================
 * 🧩 Create Promise
 * ============================================================
 */

const task = new Promise(function (resolve, reject) {
  const success = false;

  if (success) {
    resolve("Task Successful");
  } else {
    reject("Task Failed");
  }
});

/**
 * ============================================================
 * 🧩 Handle Promise
 * ============================================================
 */

task
  .then(function (data) {
    console.log(data);
  })

  .catch(function (error) {
    console.log(error);
  });

