/**
 * ============================================================
 * 📘 Callback Functions
 * ============================================================
 */

function processUser(callback) {
  console.log("Processing User");

  callback();
}

/**
 * ============================================================
 * 🧩 Callback Function
 * ============================================================
 */

function completed() {
  console.log("Task Completed");
}

processUser(completed);

