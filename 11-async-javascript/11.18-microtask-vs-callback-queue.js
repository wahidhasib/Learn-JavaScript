/**
 * ============================================================
 * 📘 Microtask Queue vs Callback Queue
 * ============================================================
 */

console.log("Start");

/**
 * ============================================================
 * 🧩 Callback Queue
 * ============================================================
 */

setTimeout(function () {
  console.log("Timeout");
}, 0);

console.log("End");

/**
 * ============================================================
 * 🧩 Microtask Queue
 * ============================================================
 */

Promise.resolve().then(function () {
  console.log("Promise");
});

/**
 * ============================================================
 * 🧠 Output Order
 * ============================================================
 *
 * Start
 * End
 * Promise
 * Timeout
 */

