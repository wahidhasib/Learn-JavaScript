/**
 * ============================================================
 * 📘 Dynamic Typing
 * ============================================================
 *
 * JavaScript dynamically typed language
 *
 * অর্থ:
 * → variable এর type runtime এ change হতে পারে
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

let data = "Hasib";

console.log(data);

data = 25;

console.log(data);

data = true;

console.log(data);

/**
 * ============================================================
 * ⚠️ IMPORTANT NOTE
 * ============================================================
 *
 * একই variable এ different type রাখা যায়
 */

/**
 * ============================================================
 * 🧠 REAL-LIFE ANALOGY
 * ============================================================
 *
 * Variable = Box
 *
 * আজকে box এ বই
 * কালকে box এ mobile
 *
 * একই box different জিনিস রাখতে পারে
 */

