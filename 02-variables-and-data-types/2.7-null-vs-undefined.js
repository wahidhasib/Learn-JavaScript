/**
 * ============================================================
 * 📘 null vs undefined
 * ============================================================
 */

/**
 * ============================================================
 * 🧩 undefined
 * ============================================================
 *
 * Variable declare করা হয়েছে
 * কিন্তু value assign করা হয়নি
 */

let name;

console.log(name);

/**
 * ============================================================
 * 🧩 null
 * ============================================================
 *
 * Intentionally empty value
 */

const selectedUser = null;

console.log(selectedUser);

/**
 * ============================================================
 * ⚠️ IMPORTANT DIFFERENCE
 * ============================================================
 *
 * undefined
 * → JavaScript automatically দেয়
 *
 * null
 * → developer manually assign করে
 */

/**
 * ============================================================
 * 🧠 REAL-LIFE ANALOGY
 * ============================================================
 *
 * undefined
 * → Empty room (কেউ আসেনি)
 *
 * null
 * → Reserved room কিন্তু intentionally empty
 *
 */

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * undefined = no value assigned
 * null      = intentionally empty
 *
 * ============================================================
 */
