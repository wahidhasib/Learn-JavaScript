/**
 * ============================================================
 * 📘 Primitive Data Types
 * ============================================================
 *
 * JavaScript Primitive Types:
 *
 * 1. String
 * 2. Number
 * 3. Boolean
 * 4. Undefined
 * 5. Null
 * 6. BigInt
 * 7. Symbol
 */

/**
 * ============================================================
 * 🧩 1. String
 * ============================================================
 */

const name = "Hasib";

console.log(name);

/**
 * ============================================================
 * 🧩 2. Number
 * ============================================================
 */

const age = 25;
const price = 99.99;

console.log(age);
console.log(price);

/**
 * ============================================================
 * 🧩 3. Boolean
 * ============================================================
 */

const isAdmin = true;

console.log(isAdmin);

/**
 * ============================================================
 * 🧩 4. Undefined
 * ============================================================
 */

let user;

console.log(user);

/**
 * ============================================================
 * 🧩 5. Null
 * ============================================================
 */

const selectedUser = null;

console.log(selectedUser);

/**
 * ============================================================
 * 🧩 6. BigInt
 * ============================================================
 */

const bigNumber = 12345678901234567890n;

console.log(bigNumber);

/**
 * ============================================================
 * 🧩 7. Symbol
 * ============================================================
 */

const id = Symbol("id");

console.log(id);

/**
 * ============================================================
 * * Primitive data (Copy by value)
 * ============================================================
 */

let username = "Wahidhasib6@gmail.com";
username[0] = "w";

console.log(username);
/**
 * We can't change this value instead of W
 * because Primitive stores value on ram by the value instead of using position
 */

