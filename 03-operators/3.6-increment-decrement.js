/**
 * ============================================================
 * 📘 Increment & Decrement Operators
 * ============================================================
 */

/**
 * ============================================================
 * 🧩 Increment (++)
 * ============================================================
 */

let count = 1;

count++;

console.log(count);

/**
 * ============================================================
 * 🧩 Decrement (--)
 * ============================================================
 */

count--;

console.log(count);

/**
 * ============================================================
 * 🧩 Prefix Increment
 * ============================================================
 */

let x = 5;

console.log(++x);

/**
 * ============================================================
 * 🧩 Postfix Increment
 * ============================================================
 */

let y = 5;

console.log(y++);

console.log(y);

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * ++ → increase by 1
 * -- → decrease by 1
 *
 * ============================================================
 */
