/**
 * ============================================================
 * 📘 Comparison Operators
 * ============================================================
 *
 * দুইটি value compare করতে use হয়
 *
 * Result:
 * → true
 * → false
 */

/**
 * ============================================================
 * 🧩 Equal (==)
 * ============================================================
 */

console.log(10 == "10");

/**
 * ⚠️ Type convert করে compare করে
 */

/**
 * ============================================================
 * 🧩 Strict Equal (===)
 * ============================================================
 */

console.log(10 === "10");

/**
 * ✅ Value + Type দুটো compare করে
 */

/**
 * ============================================================
 * 🧩 Not Equal (!=)
 * ============================================================
 */

console.log(10 != 5);

/**
 * ============================================================
 * 🧩 Strict Not Equal (!==)
 * ============================================================
 */

console.log(10 !== "10");

/**
 * ============================================================
 * 🧩 Greater Than (>)
 * ============================================================
 */

console.log(20 > 10);

/**
 * ============================================================
 * 🧩 Less Than (<)
 * ============================================================
 */

console.log(5 < 10);

/**
 * ============================================================
 * 🧩 Greater Than Equal (>=)
 * ============================================================
 */

console.log(10 >= 10);

/**
 * ============================================================
 * 🧩 Less Than Equal (<=)
 * ============================================================
 */

console.log(5 <= 10);

/**
 * ============================================================
 * 🎯 BEST PRACTICE
 * ============================================================
 *
 * Always prefer:
 * → ===
 * → !==
 */

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * Comparison operators:
 * → true/false return করে
 *
 * ============================================================
 */
