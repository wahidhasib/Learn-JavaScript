/**
 * ============================================================
 * 📘 JavaScript Arithmetic Operators
 * ============================================================
 *
 * Arithmetic operators ব্যবহার করা হয়:
 * → Mathematical calculation করার জন্য
 */

/**
 * ============================================================
 * 🧩 1. Addition (+)
 * ============================================================
 */

const a = 10;
const b = 5;

console.log(a + b);

/**
 * ============================================================
 * 🧩 2. Subtraction (-)
 * ============================================================
 */

console.log(a - b);

/**
 * ============================================================
 * 🧩 3. Multiplication (*)
 * ============================================================
 */

console.log(a * b);

/**
 * ============================================================
 * 🧩 4. Division (/)
 * ============================================================
 */

console.log(a / b);

/**
 * ============================================================
 * 🧩 5. Modulus (%)
 * ============================================================
 *
 * ভাগশেষ বের করে
 */

console.log(a % b);

/**
 * ============================================================
 * 🧩 6. Exponentiation (**)
 * ============================================================
 *
 * Power calculation
 */

console.log(2 ** 8);

