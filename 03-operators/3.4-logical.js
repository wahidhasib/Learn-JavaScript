/**
 * ============================================================
 * 📘 Logical Operators
 * ============================================================
 *
 * Multiple condition নিয়ে কাজ করতে use হয়
 */

/**
 * ============================================================
 * 🧩 AND (&&)
 * ============================================================
 *
 * সব condition true হতে হবে
 */

const age = 25;
const hasPermission = true;

console.log(age >= 18 && hasPermission);

/**
 * ============================================================
 * 🧩 OR (||)
 * ============================================================
 *
 * যেকোনো একটি true হলেই হবে
 */

const isAdmin = false;
const isModerator = true;

console.log(isAdmin || isModerator);

/**
 * ============================================================
 * 🧩 NOT (!)
 * ============================================================
 *
 * true → false
 * false → true
 */

const isLoggedIn = false;

console.log(!isLoggedIn);

/**
 * ============================================================
 * 🧠 REAL-LIFE ANALOGY
 * ============================================================
 *
 * &&
 * → দুইটা key লাগবে
 *
 * ||
 * → যেকোনো একটা key হলেই হবে
 *
 */

