/**
 * ============================================================
 * 🧩 Basic Assignment
 * ============================================================
 */

let number = 10;

console.log(number);

/**
 * ============================================================
 * 🧩 Addition Assignment
 * ============================================================
 */

number += 5;

console.log(number);

/**
 * ============================================================
 * 🧩 Subtraction Assignment
 * ============================================================
 */

number -= 3;

console.log(number);

/**
 * ============================================================
 * 🧩 Multiplication Assignment
 * ============================================================
 */

number *= 2;

console.log(number);

/**
 * ============================================================
 * 🧩 Division Assignment
 * ============================================================
 */

number /= 4;

console.log(number);

