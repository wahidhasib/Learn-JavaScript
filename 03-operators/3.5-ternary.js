/**
 * ============================================================
 * 📘 Ternary Operator
 * ============================================================
 *
 * Short if-else
 */

/**
 * ============================================================
 * 🧩 Basic Example
 * ============================================================
 */

const age = 20;

const result = age >= 18 ? "Adult" : "Minor";

console.log(result);

/**
 * ============================================================
 * 🧠 Syntax
 * ============================================================
 *
 * condition
 * ? true value
 * : false value
 *
 */

