/**
 * ============================================================
 * 📘 Operator Precedence
 * ============================================================
 *
 * কোন operator আগে execute হবে
 * তা নির্ধারণ করে
 */

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const result = 10 + 5 * 2;

console.log(result);

/**
 * First:
 * → 5 * 2
 *
 * Then:
 * → 10 + 10
 */

/**
 * ============================================================
 * 🧩 Using Parentheses
 * ============================================================
 */

const total = (10 + 5) * 2;

console.log(total);

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * Parentheses use করলে code readable হয়
 *
 * ============================================================
 */
