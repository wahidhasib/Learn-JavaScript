/**
 * ============================================================
 * 📘 Object Reference Behavior
 * ============================================================
 */

const user1 = {
  name: "Hasib",
};

const user2 = user1;

/**
 * ============================================================
 * 🧩 Modify Second Object
 * ============================================================
 */

user2.name = "Rakib";

console.log(user1);

console.log(user2);

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * Object reference type
 *
 * একই memory reference share করে
 */

/**
 * ============================================================
 * 🧩 Proper Copy
 * ============================================================
 */

const original = {
  language: "JavaScript",
};

const copied = { ...original };

copied.language = "PHP";

console.log(original);

console.log(copied);

