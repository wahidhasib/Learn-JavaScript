/**
 * ============================================================
 * 📘 Object Looping
 * ============================================================
 */

const user = {
  name: "Hasib",
  age: 25,
  country: "Bangladesh",
};

/**
 * ============================================================
 * 🧩 for...in
 * ============================================================
 */

for (const key in user) {
  console.log(key, ":", user[key]);
}

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * for...in:
 * → object keys loop করে
 *
 * ============================================================
 */
