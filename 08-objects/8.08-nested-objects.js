/**
 * ============================================================
 * 📘 Nested Objects
 * ============================================================
 *
 * Object এর ভিতরে object
 */

const user = {
  name: "Hasib",

  address: {
    city: "Dhaka",
    zip: 1207,
  },
};

/**
 * ============================================================
 * 🧩 Access Nested Data
 * ============================================================
 */

console.log(user.name); // Hasib

console.log(user.address.zip); // 1207

