/**
 * ============================================================
 * 📘 Add / Update / Delete Properties
 * ============================================================
 */

const product = {
  name: "Laptop",
  price: 50000,
};

/**
 * ============================================================
 * 🧩 Add Property
 * ============================================================
 */

product.brand = "Dell";
product.market = "Asia";

console.table(product);

/**
 * ============================================================
 * 🧩 Update Property
 * ============================================================
 */

product.price = 60000;

console.table(product);

/**
 * ============================================================
 * 🧩 Delete Property
 * ============================================================
 */

delete product.brand;
delete product["market"];

console.table(product);

