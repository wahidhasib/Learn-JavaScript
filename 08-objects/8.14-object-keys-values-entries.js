/**
 * ============================================================
 * 📘 Object.keys() / values() / entries()
 * ============================================================
 */

const user = {
  name: "Hasib",
  age: 25,
};

/**
 * ============================================================
 * 🧩 Object.keys()
 * ============================================================
 */

console.log(Object.keys(user));

/**
 * ============================================================
 * 🧩 Object.values()
 * ============================================================
 */

console.log(Object.values(user));

/**
 * ============================================================
 * 🧩 Object.entries()
 * ============================================================
 */

console.log(Object.entries(user));

