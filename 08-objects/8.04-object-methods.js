/**
 * ============================================================
 * 📘 Object Methods
 * ============================================================
 *
 * Object এর ভিতরে function থাকলে
 * তাকে method বলে
 */

const user = {
  name: "Hasib",

  greet: function () {
    console.log("Hello", this.name);
  },
};

user.greet();

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * Object function = method
 *
 * ============================================================
 */
