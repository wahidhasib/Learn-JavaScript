/**
 * ============================================================
 * 📘 Spread Operator with Objects
 * ============================================================
 */

const user = {
  name: "Hasib",
};

const extra = {
  country: "Bangladesh",
};

/**
 * ============================================================
 * 🧩 Merge Objects
 * ============================================================
 */

const merged = {
  ...user,
  ...extra,
};

console.log(merged);

/**
 * ============================================================
 * 🧩 Clone Object
 * ============================================================
 */

const copied = {
  ...user,
};

console.log(copied);

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * Spread operator:
 * → merge & clone করতে useful
 *
 * ============================================================
 */
