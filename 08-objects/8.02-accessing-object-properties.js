/**
 * ============================================================
 * 📘 Accessing Object Properties
 * ============================================================
 */

const user = {
  name: "Hasib",
  age: 25,
};

/**
 * ============================================================
 * 🧩 Dot Notation
 * ============================================================
 */

console.log(user.name);

/**
 * ============================================================
 * 🧩 Bracket Notation
 * ============================================================
 */

console.log(user["age"]);

/**
 * ============================================================
 * 🧩 Dynamic Property
 * ============================================================
 */

const key = "name";

console.log(user[key]);

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * Dot notation:
 * → normal access
 *
 * Bracket notation:
 * → dynamic access
 *
 * ============================================================
 */
