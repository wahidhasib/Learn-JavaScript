/**
 * ============================================================
 * 📘 JSON Basics
 * ============================================================
 *
 * JSON:
 * → JavaScript Object Notation
 */

/**
 * ============================================================
 * 🧩 JavaScript Object
 * ============================================================
 */

const user = {
  name: "Hasib",
  age: 25,
};

/**
 * ============================================================
 * 🧩 Convert Object → JSON (Look like normal string)
 * ============================================================
 */

const jsonData = JSON.stringify(user);

console.log(jsonData);

/**
 * ============================================================
 * 🧩 Convert JSON → Object
 * ============================================================
 */

const parsedData = JSON.parse(jsonData);

console.log(parsedData);

