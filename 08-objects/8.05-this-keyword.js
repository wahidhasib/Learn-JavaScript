/**
 * ============================================================
 * 📘 this Keyword
 * ============================================================
 *
 * this:
 * → current object কে refer করে
 */

const student = {
  name: "Hasib",
  age: 25,

  introduce() {
    console.log(`My name is ${this.name}`);
  },
};

student.introduce();

