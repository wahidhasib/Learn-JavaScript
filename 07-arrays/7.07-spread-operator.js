/**
 * ============================================================
 * 📘 Spread Operator (...)
 * ============================================================
 *
 * Array copy/merge করতে use হয়
 */

const numbers1 = [1, 2, 3];

const numbers2 = [4, 5, 6];

/**
 * ============================================================
 * 🧩 Merge Arrays
 * ============================================================
 */

const merged = [...numbers1, ...numbers2];

// console.log(merged);
// console.log(numbers1);

/**
 * ============================================================
 * 🧩 Copy Array
 * ============================================================
 */

const copied = [...numbers1]; //copy of numbers1 array

numbers1[1] = 5;
console.log(copied);
console.log(numbers1);

