/**
 * ============================================================
 * 📘 map()
 * ============================================================
 *
 * প্রতিটি element modify করে
 * নতুন array return করে
 */

const numbers = [1, 2, 3, 4];

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const doubled = numbers.map((num) => {
  return num * 2;
});

console.log("Original value", numbers);
console.log("doubled value", doubled);

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * Original array change হয় না
 */

