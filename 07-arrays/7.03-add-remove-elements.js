/**
 * ============================================================
 * 📘 Add & Remove Array Elements
 * ============================================================
 */

const fruits = ["Apple", "Banana"];

/**
 * ============================================================
 * 🧩 push()
 * ============================================================
 *
 * শেষের দিকে add করে
 */

fruits.push("Orange");

console.log(fruits);

/**
 * ============================================================
 * 🧩 pop()
 * ============================================================
 *
 * শেষের element remove করে
 */

fruits.pop();

console.log(fruits);

/**
 * ============================================================
 * 🧩 unshift()
 * ============================================================
 *
 * শুরুর দিকে add করে
 */

fruits.unshift("Mango");

console.log(fruits);

/**
 * ============================================================
 * 🧩 shift()
 * ============================================================
 *
 * শুরুর element remove করে
 */

fruits.shift();

console.log(fruits);

/**
 * ============================================================
 * 🎯 SUMMARY
 * ============================================================
 *
 * push/pop → end
 * shift/unshift → start
 *
 * ============================================================
 */
