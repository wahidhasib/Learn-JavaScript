/**
 * ============================================================
 * 📘 Array Destructuring
 * ============================================================
 *
 * Array value সহজভাবে variable এ নেওয়া
 */

const colors = ["Red", "Green", "Blue"];

/**
 * ============================================================
 * 🧩 Example
 * ============================================================
 */

const [first, second, third, forth] = colors;

console.log(first);

console.log(second);

console.log(third);

console.log(forth); // undefined (not exists)

/**
 * ============================================================
 * 🧩 Skip Value
 * ============================================================
 */

const [a, , c] = colors;

console.log(a);

console.log(c);

