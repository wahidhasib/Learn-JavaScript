/**
 * ============================================================
 * 📘 Array Reference Behavior
 * ============================================================
 */

const arr1 = [1, 2, 3];

const arr2 = arr1;

/**
 * ============================================================
 * 🧩 Modify Second Array
 * ============================================================
 */

arr2.push(4);

console.log(arr1);

console.log(arr2);

/**
 * ============================================================
 * ⚠️ IMPORTANT
 * ============================================================
 *
 * Array reference type
 *
 * একই memory reference share করে
 */

/**
 * ============================================================
 * 🧩 Proper Copy
 * ============================================================
 */

const original = [10, 20];

const copied = [...original];

copied.push(30);

console.log(original);

console.log(copied);

